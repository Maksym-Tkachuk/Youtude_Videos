function formatValue(n: number): string {
  if (n >= 1000) return (n / 1000).toFixed(1) + "B";
  return Math.round(n) + "M";
}

function formatValueFull(n: number): string {
  return Math.round(n * 1_000_000).toLocaleString("en-US");
}

