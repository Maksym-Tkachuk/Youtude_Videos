function colorScale(value: number, _maxValue: number): string {
  if (value <= 0) return "#152238";
  const t = Math.min(1, value / 50);
  if (t < 0.2) {
    const s = t / 0.2;
    return `rgb(${Math.round(20 + s * 30)}, ${Math.round(60 + s * 60)}, ${Math.round(100 + s * 20)})`;
  }
  if (t < 0.5) {
    const s = (t - 0.2) / 0.3;
    return `rgb(${Math.round(50 + s * 180)}, ${Math.round(120 + s * 50)}, ${Math.round(120 - s * 60)})`;
  }
  const s = (t - 0.5) / 0.5;
  return `rgb(${Math.round(230 + s * 25)}, ${Math.round(170 - s * 140)}, ${Math.round(60 - s * 50)})`;
}

