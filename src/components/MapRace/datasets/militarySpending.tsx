function colorScale(value: number, _maxValue: number): string {
  if (value <= 0) return "#152238";
  const t = Math.min(1, value / 15); // 15% GDP = max red
  if (t < 0.2) {
    return `rgb(${Math.round(20 + t * 150)}, ${Math.round(60 + t * 200)}, ${Math.round(120 + t * 100)})`;
  }
  if (t < 0.5) {
    const s = (t - 0.2) / 0.3;
    return `rgb(${Math.round(50 + s * 200)}, ${Math.round(100 + s * 130)}, ${Math.round(140 - s * 80)})`;
  }
  const s = (t - 0.5) / 0.5;
  return `rgb(${Math.round(250)}, ${Math.round(230 - s * 180)}, ${Math.round(60 - s * 50)})`;
}

function formatValue(n: number): string {
  return `${n.toFixed(1)}%`;
}

