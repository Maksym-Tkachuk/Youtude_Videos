function colorScale(value: number, _maxValue: number): string {
  if (value <= 0) return "#152238";
  const t = Math.min(1, value / 1.2); // 1.2% = max green
  if (t < 0.3) {
    const s = t / 0.3;
    return `rgb(${Math.round(20 + s * 10)}, ${Math.round(50 + s * 80)}, ${Math.round(80 + s * 30)})`;
  }
  if (t < 0.6) {
    const s = (t - 0.3) / 0.3;
    return `rgb(${Math.round(30 + s * 20)}, ${Math.round(130 + s * 60)}, ${Math.round(110 - s * 20)})`;
  }
  const s = (t - 0.6) / 0.4;
  return `rgb(${Math.round(50 + s * 30)}, ${Math.round(190 + s * 60)}, ${Math.round(90 - s * 30)})`;
}

