function colorScale(value: number, _maxValue: number): string {
  if (value <= 0) return "#152238";
  const t = Math.min(1, value / 85);
  if (t < 0.3) {
    const s = t / 0.3;
    return `rgb(${Math.round(20 + s * 40)}, ${Math.round(60 + s * 80)}, ${Math.round(100 + s * 30)})`;
  }
  if (t < 0.6) {
    const s = (t - 0.3) / 0.3;
    return `rgb(${Math.round(60 + s * 180)}, ${Math.round(140 + s * 40)}, ${Math.round(130 - s * 70)})`;
  }
  const s = (t - 0.6) / 0.4;
  return `rgb(${Math.round(240 + s * 15)}, ${Math.round(180 - s * 140)}, ${Math.round(60 - s * 50)})`;
}

function formatValue(n: number): string {
  return `${Math.round(100 - n)}/100`;
}

